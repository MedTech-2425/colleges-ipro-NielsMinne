#Logical operators
#Boolean is een waarde die waar of onwaar kan zijn

x = True
y = False
print(x and y)  # and = Beide waar
print(x or y)   # or = Minstens één waar
print(not x)    # Omkeren van de waarde (true wordt false, false wordt true)

