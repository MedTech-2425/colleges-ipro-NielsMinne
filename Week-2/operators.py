#Relational operators

a = 10
b = 20

print(a == b)  # Gelijkheid controleren
print(a != b)  # Ongelijkheid controleren
print(a > b)   # Groter dan
print(a < b)   # Kleiner dan
print(a >= b)  # Groter dan of gelijk
print(a <= b)  # Kleiner dan of gelijk

